public class SumOfNumbers1  
{  
public static void main(String args[])   
{  

float sum = 0;
float x1 = 0.2f;
float x2 = 2/3f;
float x3 = 0.3f;

sum = x1 * x2 - x3;  
System.out.println("The sum of numbers is: "+sum);  
//This will print the result followed by a new line.

System.out.print("The sum of numbers is: "+sum);
//This will print the result without moving to a new line.

System.out.printf("The sum of numbers is: %f", +sum);
//Thiss will print the result as a var-type (in this case as an double with 6 digits).
    
}  
}  
